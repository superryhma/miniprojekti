package com.github.superryhma.miniprojekti.models;

public class AttributeType {

	protected int id;
	protected String type;

	public AttributeType() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
